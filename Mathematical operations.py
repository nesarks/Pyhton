# Mathematical Operations

# Addition
a = 10
b = 20
print(a + b) # 30

# Subtraction
a = 10
b = 20
print(b - a) # 10

# Multiplication
a = 10
b = 20
print(a * b) # 200

# Division
a = 10
b = 20
print(b / a) # 2.0

# Modulo
a = 10
b = 20
print(b % a) # 0

# Power
a = 10
b = 2
print(a ** b) # 100

# Operator Precedence
a = 10
b = 20
c = 30

# Without Parentheses
print(a + b * c) # 210

# With Parentheses
print((a + b) * c) # 900